// Classes
Servo lid;
Servo lock;
AsyncWebServer server(80);
Adafruit_SSD1306 display(SCREEN_WIDTH, SCREEN_HEIGHT, &Wire, OLED_RESET);

void buzzer() {
  if (isOpen) {
    Serial.println("Opening sound");
    ledcAttachPin(BUZZER, 12);
    ledcWriteNote(12, NOTE_C, 8);
    delay(100);
    ledcDetachPin(BUZZER);
    delay(50);
    ledcAttachPin(BUZZER, 12);
    ledcWriteNote(12, NOTE_C, 8);
    delay(100);
    ledcDetachPin(BUZZER);
    delay(50);

  } else {
    Serial.println("Closing sound");
    ledcAttachPin(BUZZER, 12);
    ledcWriteNote(12, NOTE_C, 8);
    delay(100);
    ledcDetachPin(BUZZER);
    delay(50);
    ledcAttachPin(BUZZER, 12);
    ledcWriteNote(12, NOTE_C, 6);
    delay(100);
    ledcDetachPin(BUZZER);
    delay(50);
  }
}

void updateServo() {
  if (isOpen) {
    Serial.println("Opening box");
    lock.write(180);
    delay(1000);

    for (int pos = 60; pos <= 160; pos += 1) {
      lid.write(pos);
    }
  } else {
    Serial.println("Closing box");
    for (int pos = 160; pos >= 60; pos -= 1) {
      lid.write(pos);
    }
    delay(1000);

    lock.write(8);
  }
}

void led(int color) {
  if (color == LEDR) {
    digitalWrite(LEDR, HIGH);
    digitalWrite(LEDG, LOW);
    digitalWrite(LEDB, LOW);
  }
  if (color == LEDG) {
    digitalWrite(LEDR, LOW);
    digitalWrite(LEDG, HIGH);
    digitalWrite(LEDB, LOW);
    lastLED = color;
  }
  if (color == LEDB) {
    digitalWrite(LEDR, LOW);
    digitalWrite(LEDG, LOW);
    digitalWrite(LEDB, HIGH);
    lastLED = color;
  }
  if (color == -1) {
    led(lastLED);
  }
}

void updateLed() {
  if (isOpen) {
    Serial.println("Green led");
    led(LEDG);
  } else {
    Serial.println("Blue led");
    led(LEDB);
  }
}

void setup() {
  Serial.begin(9600);

  pinMode(LEDR, OUTPUT);
  pinMode(LEDG, OUTPUT);
  pinMode(LEDB, OUTPUT);
  pinMode(BUZZER, OUTPUT);

  // Servo setup
  lid.attach(LID, MIN_PULSE_WIDTH, MAX_PULSE_WIDTH);
  lock.attach(LOCK, MIN_PULSE_WIDTH, MAX_PULSE_WIDTH);

  // Wifi setup
  Serial.print("Red led");
  led(LEDR);
  Serial.println(WIFI_NAME);
  WiFi.begin(WIFI_NAME, WIFI_PASSWORD);
  while (WiFi.status() != WL_CONNECTED) {
    Serial.print('.');
    delay(1000);
  }
  IPAddress ip = WiFi.localIP();
  Serial.println("Blue led");
  led(LEDB);
  Serial.println(ip);

  // Display setup
  display.begin(SSD1306_SWITCHCAPVCC, 0x3C);
  display.clearDisplay();
  display.setTextSize(1);
  display.setTextColor(WHITE);
  display.setCursor(0, 0);
  display.print(title);
  display.setCursor(90, 0);
  display.print(ip[2]);
  display.print(".");
  display.print(ip[3]);

  // GET endpoint
  server.on("/", HTTP_GET, [](AsyncWebServerRequest* request) {
    // Set open/closed state
    isOpen = !isOpen;

    buzzer();
    updateServo();
    updateLed();

    request->send(200, "text/plain", "message received");
  });

  // POST display endpoint
  server.on("/", HTTP_POST, [](AsyncWebServerRequest* request) {
    AsyncWebParameter* p = request->getParam(0);

    String data = p->value();
    char d;

    d = data[0];

    // Serial.printf("%s", &d);

    request->send(200, "text/plain", "received");
  });

  server.begin();
}

void displayAnimation() {
  display.display();
  display.drawBitmap(0, 16, epd_bitmap_default, 128, 48, WHITE);
}

void loop() {
  unsigned long currentMillis = millis();

  displayAnimation();

  // Tries reconnecting when Wifi fails
  if ((WiFi.status() != WL_CONNECTED) &&
      (currentMillis - previousMillis >= interval)) {
    Serial.print(millis());
    Serial.println("Red led");
    led(LEDR);
    WiFi.disconnect();
    WiFi.reconnect();
    previousMillis = currentMillis;
  } else if (WiFi.status() == WL_CONNECTED) {
    led(-1);
  }
}
