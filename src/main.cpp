// Classes
AsyncWebServer server(80);
Adafruit_SSD1306 display(SCREEN_WIDTH, SCREEN_HEIGHT, &Wire, OLED_RESET);

void buzzer() {
  if (isOpen) {
    Serial.println("Opening sound");
    ledcAttachPin(BUZZER, 1);
    ledcWriteNote(1, NOTE_C, 8);
    delay(100);
    ledcDetachPin(BUZZER);
    delay(50);
    ledcAttachPin(BUZZER, 1);
    ledcWriteNote(1, NOTE_C, 8);
    delay(100);
    ledcDetachPin(BUZZER);
    delay(50);

  } else {
    Serial.println("Closing sound");
    ledcAttachPin(BUZZER, 1);
    ledcWriteNote(1, NOTE_C, 8);
    delay(100);
    ledcDetachPin(BUZZER);
    delay(50);
    ledcAttachPin(BUZZER, 1);
    ledcWriteNote(1, NOTE_C, 6);
    delay(100);
    ledcDetachPin(BUZZER);
    delay(50);
  }
}

void servo() {
  if (isOpen) {
    Serial.println("Opening box");
  } else {
    Serial.println("Closing box");
  }
}

void led() {
  if (isOpen) {
    Serial.println("Green led");
  } else {
    Serial.println("Blue led");
  }
}

void updateDisplay() {
  if (isOpen) {
    Serial.println("Opening animation");
  } else {
    Serial.println("Closing animation");
  }
}

void setup() {
  Serial.begin(115200);

  pinMode(LED, OUTPUT);
  pinMode(BUZZER, OUTPUT);

  // Wifi setup
  Serial.print("Yellow led");
  Serial.println(WIFI_NAME);
  WiFi.begin(WIFI_NAME, WIFI_PASSWORD);
  while (WiFi.status() != WL_CONNECTED) {
    Serial.print('.');
    delay(1000);
  }
  IPAddress ip = WiFi.localIP();
  Serial.println("Blue led");
  Serial.println(ip);

  // Display setup
  display.begin(SSD1306_SWITCHCAPVCC, 0x3C);
  display.clearDisplay();
  display.setTextSize(1);
  display.setTextColor(WHITE);
  display.setCursor(110, 0);
  display.println(ip[3]);

  // GET endpoint
  server.on("/", HTTP_GET, [](AsyncWebServerRequest* request) {
    // Set open/closed state
    isOpen = !isOpen;
    digitalWrite(LED, isOpen);

    buzzer();
    servo();
    led();
    updateDisplay();

    request->send(200, "text/plain", "message received");
  });

  // POST display endpoint
  server.on("/", HTTP_POST, [](AsyncWebServerRequest* request) {
    int paramsNr = request->params();
    Serial.println(paramsNr);

    for (int i = 0; i < paramsNr; i++) {
      AsyncWebParameter* p = request->getParam(i);
      Serial.print("Param name: ");
      Serial.println(p->name());
      Serial.print("Param value: ");
      Serial.println(p->value());
      Serial.println("------");
    }

    // animate display

    request->send(200, "text/plain", "message received");
  });

  server.begin();
}

void displayAnimation() {
  display.display();
  display.drawBitmap(0, 16, epd_bitmap_teste, 128, 48, WHITE);
}

void loop() {
  unsigned long currentMillis = millis();

  displayAnimation();

  // Tries reconnecting when Wifi fails
  if ((WiFi.status() != WL_CONNECTED) &&
      (currentMillis - previousMillis >= interval)) {
    Serial.print(millis());
    Serial.println("Yellow led");
    WiFi.disconnect();
    WiFi.reconnect();
    previousMillis = currentMillis;
  } else if (WiFi.status() == WL_CONNECTED) {
    // sets led to current state
  }
}
